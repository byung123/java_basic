package exam;

public class Example08 {
	public static void main(String[] args) {
		/*
		 * 컬렉션 프레임워크
		 * - 자료구조는 프로그램 실행 시 메모리에 자료를 유지하고 관리하기 위해 사용한다
		 * - 배열은 정한 크기를 변경하거나 삭제할 수 없다
		 *   그래서 자바는 배열의 한계점을 극복하기 위해 필요한 자료구조를 java.util 패키지에서 제공하고 있다
		 *   
		 * 즉) 컬렉션 프레임워크란 기존에 존재했던 자료 구조에 인터펭리스로 설계된 기능을
		 *    클래스를 통해 제공하며 제이터 관리에 용이한 자료구조객체를 구조화 한 것
		 *      
		 * 인터페이스 : List, Set, Map
		 * 
		 * List : 데이터 중복 허용, 순서가 있는 데이터 집합
		 *        구현 클래스(ArrayList, LinkedList)
		 * Set : 데이터 중복 허용 안함, 순서 없음
		 *       구현 클래스(HashSet, LinkedHashSet)
		 *       
		 * Map : 
		 * 키(key), 값(value)의 ㅛㅏㅇ으로 이루어진 데이터 집합
		 * 순서가 없고, 키는 중복이 불가능하다, 값은 중복 가능하다
		 * HashMap, HashTable, LinkedHashMap
		 */
	}
}
