public class Quiz4 {
	public static void main(String[] args) {
		/*
		 
		 * 
		 **
		 ***
		 ****
		 *****
		 
		 */
		
		//내가 쓴 답
		for(int i = 1; i <= 5; i++) {
			for(int j = 1; j <= i; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		//강사님 답
		//나와 같음
	}
}
